    void timerHandler(){
        int Value=this.jProgressBar1.getValue();
        if(Value==100){ // TerminÃ³
            this.setVisible(false);
            jButton2.setVisible(true);
            parent.setVisible(true);
        }
        else{
            this.jProgressBar1.setValue(Value+10);
            this.jLabel3.setText("Cargando: "+(Value+10)+"%");
        }
    }

